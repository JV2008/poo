package POO2;

public class Usuario {
	int id;
	String nome;
	int senha;
	String permissão;
	String usuário;
	String logar;
	String deslogar;
	String pesquisar;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getSenha() {
		return senha;
	}
	public void setSenha(int senha) {
		this.senha = senha;
	}
	public String getPermissão() {
		return permissão;
	}
	public void setPermissão(String permissão) {
		this.permissão = permissão;
	}
	public String getUsuário() {
		return usuário;
	}
	public void setUsuário(String usuário) {
		this.usuário = usuário;
	}
	public String getLogar() {
		return logar;
	}
	public void setLogar(String logar) {
		this.logar = logar;
	}
	public String getDeslogar() {
		return deslogar;
	}
	public void setDeslogar(String deslogar) {
		this.deslogar = deslogar;
	}
	public String getPesquisar() {
		return pesquisar;
	}
	public void setPesquisar(String pesquisar) {
		this.pesquisar = pesquisar;
	}
}



